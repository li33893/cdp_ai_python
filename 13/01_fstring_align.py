"""

f-string 的对齐格式
你平时写 f"{x}" 是把 x 的值填进去。如果在变量名后面加一个冒号 :，冒号后面就能写"格式要求"，其中一种就是对齐。
格式是这样：: + 对齐方向 + 宽度数字。
对齐方向有三个符号：

< 靠左（内容在左，右边补空格）
> 靠右（内容在右，左边补空格）
^ 居中（两边补空格）

宽度数字是指这一格至少占多少个字符。举几个具体的，假设 x = "ES"：
f"{x:>6}" → "    ES"（右对齐，占 6 格，左边补 4 个空格）
f"{x:<6}" → "ES    "（左对齐，右边补 4 个空格）
f"{x:^6}" → "  ES  "（居中，两边各补 2 个空格）
一个要记住的点：宽度是"至少"的意思。如果内容本身比宽度还长，它不会被砍掉，会原样全部打印出来。

居中 ^ 要补的空格是"总宽 − 内容长度"，这些空格要分到左右两边。
分两种情况：
空格总数是偶数时，两边平分。比如内容 2 个字符、总宽 6，补 4 个空格，左右各 2 个。
空格总数是奇数时，没法平均分，Python 的规则是左边少、右边多（多出来的那一个放右边）。比如内容 2 个字符、总宽 5，补 3 个空格，左边 1 个、右边 2 个。
就这么一条规则：偶数平分，奇数时右边多一个。

比如 f"{'Primary':>2}"，Primary 有 7 个字符，比 2 长，结果就是完整的 "Primary"，不补也不砍。
对齐的用处就是让一列列的东西右边缘（或左边缘）对齐，看起来是整齐的表格。混淆矩阵那张表就是靠这个把每一格对齐的。

"""

