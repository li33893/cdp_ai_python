"""

宽度用变量
前面几个例子里，宽度是写死的数字：f"{code:>5}" 里那个 5。
但混淆矩阵那段代码事先不知道该占多宽——宽度得看最长的标签有几个字符，是运行时才算出来的，存在 col_w 这个变量里。数字写死了不行，标签一换宽度就不对。
办法是：把宽度那个位置也用一对 {} 包起来，让 Python 先把变量的值填进去。
具体看。假设 col_w = 8，code = "TA"：
f"{code:>{col_w}}"
Python 先算里层的 {col_w}，换成 8，这一段就等价于 f"{code:>8}"，结果是 '      TA'（补 6 个空格，总宽 8）。
关键就一句：:> 后面本来跟数字，现在跟 {变量}，Python 先把变量的值填进去，再拿这个值当宽度。
代码里还有个空格子的写法：f"{'':>{col_w}}"。里头是空字符串 ''，右对齐到 col_w 宽，就是打印出 col_w 个空格——混淆矩阵表左上角空着那一格就是这么来的。

"""

# w = 4，code = "ES"。f"{code:>{w}}" 打印出什么？把空格数清楚。
# 靠右对齐， 左边空两格

# 再判断一个：如果 w 改成 1（比 "ES" 还短），会怎样？说说你的判断。
# 不空

labels = ["Primary", "NM", "Solo"]
# 写代码：先算出这批标签里最长的有几个字符，拿这个数当列宽，把每个标签右对齐、一行一个打印出来，让右边缘对齐。列宽必须随标签内容自动定，不能写死数字。

col_w = len(max(labels, key=len))
# print(col_w)
for l in labels:
    print(f"{l:>{col_w}}")