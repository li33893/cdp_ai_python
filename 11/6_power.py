# 题目2（操作题）

# 你在跑 batch_coding.py 编码 1200 条帖子。有一条帖子请求 API 失败了，脚本要反复重试同一条，但每失败一次就多等一会儿，免得把服务器逼得更忙。
# 等待规则是这样的：手里有个基数 2、一个上限 20 秒。这是第几次重试就记作 attempt（第一次重试 attempt 是 0）。
# 每次重试要等的秒数这么定——先把基数 2 自乘"attempt 那么多次"，再加 1 秒，得到一个"理想等待时间"；
# 但公司规定最多不能等超过 20 秒，所以真正等的时间是"理想等待时间"和"20 秒上限"里较小的那个。
# 现在这条帖子非常倒霉，连续失败了 6 次
# （attempt 从 0 一直到 5）。请算出这 6 次重试分别等了几秒，并说出：从第几次重试开始，因为撞到 20 秒上限，等待时间就卡死在 20 不再往上涨了。

BACKOFF_BASE = 2
BACKOFF_MAX  = 20

for attempt in range(6):
    wait = min(BACKOFF_BASE ** attempt + 1, BACKOFF_MAX)
    print(wait)




