"""

01 set() 

把一个列表转成集合，集合的特点是：自动去重、无顺序。最常用的场景是快速检查某个值是否已经出现过。

numbers = [1, 2, 2, 3, 3, 3]
unique = set(numbers)
print(unique)   # {1, 2, 3}，重复的被去掉了


02 集合运算

| 是并集，把两个集合合并，去重：
a = {1, 2, 3}
b = {3, 4, 5}
print(a | b)   # {1, 2, 3, 4, 5}

in 检查某个值是否在集合里，和列表的 in 语法一样，但集合快得多：
seen = {10, 20, 30}
print(10 in seen)   # True
print(99 in seen)   # False



03 集合和列表的三个区别

1. 顺序
列表有顺序，元素按你放进去的顺序排列，可以用下标访问 list[0]。集合没有顺序，没有下标，不能用 set[0]。

2. 重复
列表允许重复元素。集合自动去重，同一个值只保留一份。

3. 速度
检查"某个值在不在里面"时，集合比列表快很多。列表的 in 要一个一个扫，集合用哈希直接定位。collect.py 里用 seen_ids = set() 而不是列表，就是为了这个——帖子数量大的时候差距明显。
简单说：需要有序、可重复、能按位置取值，用列表。需要去重、快速查找是否存在，用集合。

"""

# 定义两个列表：batch1 = ["a1", "a2", "a3"] 和 batch2 = ["a2", "a3", "a4"]

batch1 = ["a1", "a2", "a3"] 
batch2 = ["a2", "a3", "a4"]

# 用 set() 把两个列表各转成集合，再用 | 合并成 all_ids，打印它

co_1 = set(batch1)
co_2 = set(batch2)
all_ids = co_1 | co_2
print(all_ids)

# 再写一个 seen = {"a1", "a2"}，用 in 检查 "a3" 和 "a1" 是否在 seen 里，各打印一行

seen = {"a1", "a2"}
print("a1" in seen)
print("a3" in seen)